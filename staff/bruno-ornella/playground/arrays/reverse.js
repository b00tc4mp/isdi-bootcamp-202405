console.log('CASE reverse from arrays')

var array = ['one', 'two', 'three', 'four', 'five']

console.log(array)
//['one', 'two', 'three', 'four', 'five']

var array1 = array.reverse()

console.log(array1)
//['five', 'four', 'three', 'two', 'one']

console.log(array)
//['five', 'four', 'three', 'two', 'one']
